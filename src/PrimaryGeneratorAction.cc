PrimaryGeneratorAction::PrimaryGeneratorAction()
{
  G4int n_particle = 1;
  fParticleGun  = new G4ParticleGun(n_particle);

  // default particle kinematic
  G4ParticleTable* particleTable = G4ParticleTable::GetParticleTable();
  G4String particleName;
  G4ParticleDefinition* particle
    = particleTable->FindParticle(particleName="e+");
  fParticleGun->SetParticleDefinition(particle);
  fParticleGun->SetParticleMomentumDirection(RandomDirection());
  fParticleGun->SetParticleEnergy(3.*MeV);
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

PrimaryGeneratorAction::~PrimaryGeneratorAction()
{
  delete fParticleGun;
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

G4ThreeVector PrimaryGeneratorAction::RandomDirection(){
  return G4ThreeVector(G4UniformRand() * 0.6203505, G4UniformRand() * 0.6203505, G4UniformRand() * 0.6203505);
}

G4ThreeVector PrimaryGeneratorAction::AlongParticlePosition(){
  return primaryPosition * 0.6203505;
}



void PrimaryGeneratorAction::GeneratePrimaries(G4Event* anEvent)
{

  fParticleGun->SetParticlePosition(AlongParticlePosition());

  fParticleGun->GeneratePrimaryVertex(anEvent);
}

